"""파이썬 첫 실행 예제입니다.

이 파일은 Python을 처음 실행할 때 확인하는 가장 기본적인 예제입니다.
초보자는 이 파일을 통해 아래 내용을 확인합니다.

1. `print()` 함수로 화면에 글자를 출력하는 방법
2. Python에서 숫자 계산을 바로 할 수 있다는 점
3. 코드는 위에서 아래로 한 줄씩 실행된다는 점
"""

# print()는 괄호 안에 있는 값을 터미널 화면에 출력하는 함수입니다.
# 문자열은 큰따옴표(" ") 또는 작은따옴표(' ')로 감싸서 표현합니다.
print("안녕하세요. Python 기초 과정을 시작합니다.")

# Python은 계산기처럼 숫자 연산을 바로 처리할 수 있습니다.
# 쉼표(,)를 사용하면 여러 값을 한 줄에 이어서 출력할 수 있습니다.
print("2 + 3 =", 2 + 3)
print("10 - 4 =", 10 - 4)
print("5 * 3 =", 5 * 3)
print("10 / 2 =", 10 / 2)

# Python 코드는 기본적으로 파일의 위쪽 줄부터 아래쪽 줄까지 순서대로 실행됩니다.
# 그래서 아래 세 줄은 "첫 번째 줄" -> "두 번째 줄" -> "세 번째 줄" 순서로 출력됩니다.
print("첫 번째 줄")
print("두 번째 줄")
print("세 번째 줄")
