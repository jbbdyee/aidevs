"""입력과 출력 예제입니다.

이 파일은 사용자가 직접 값을 입력하고, 그 값을 다시 화면에 출력하는 방법을 보여줍니다.
초보자는 `input()`의 결과가 항상 문자열(str)이라는 점을 기억하면 좋습니다.
"""

# input()은 터미널에서 사용자가 입력한 값을 받아옵니다.
# 괄호 안의 문장은 사용자에게 보여 줄 안내 메시지입니다.
# 사용자가 입력한 값은 name 변수에 저장됩니다.
name = input("이름을 입력하세요: ")

# print()에 여러 값을 쉼표로 나열하면 한 줄에 함께 출력됩니다.
# 쉼표로 출력하면 값 사이에 공백이 자동으로 들어갑니다.
print("반갑습니다,", name)
print(name, "님, 오늘도 Python을 연습해봅시다.")
