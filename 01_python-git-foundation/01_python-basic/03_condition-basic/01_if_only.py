"""if 단독 사용 예제입니다.

이 예제는 `if` 하나만 사용해 프로그램을 계속 진행할지,
중간에 종료할지를 결정합니다.

핵심은 다음과 같습니다.

1. 숫자를 입력받습니다.
2. 입력한 숫자가 0보다 작으면 프로그램을 종료합니다.
3. 입력한 숫자가 0보다 크면 다음 코드가 계속 실행됩니다.

`if`만 사용해도 특정 조건에서 프로그램 흐름을 멈추거나
다음 단계로 넘어가게 만들 수 있습니다.
"""

# sys 모듈은 Python이 기본으로 제공하는 모듈입니다.
# sys.exit()를 사용하면 프로그램을 즉시 종료할 수 있습니다.
import sys

# input()은 사용자에게 값을 입력받는 함수입니다.
# input()으로 받은 값은 항상 문자열(str)이므로 int()로 숫자형으로 바꿉니다.
number = int(input("숫자를 입력하세요: "))

print("입력한 숫자:", number)

# if만 사용합니다.
# number가 0보다 작으면 아래 들여쓰기 된 코드가 실행됩니다.
if number < 0:
    print("0보다 작은 수입니다.")
    print("프로그램을 종료합니다.")
    sys.exit()

# 위 if 조건이 참이면 sys.exit()에서 프로그램이 끝납니다.
# 따라서 아래 코드는 number가 0 이상일 때만 실행됩니다.
print("프로그램을 계속 진행합니다.")

# 0보다 큰 수인지 한 번 더 확인합니다.
# 이 예제에서는 if만 사용하기 위해 else를 쓰지 않습니다.
if number > 0:
    print("0보다 큰 수입니다.")

print("다음 작업을 실행할 수 있습니다.")
