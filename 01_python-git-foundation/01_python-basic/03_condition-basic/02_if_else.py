"""if와 else를 사용하는 조건문 예제입니다.

`if`는 조건이 참일 때 실행할 코드를 작성합니다.
`else`는 if 조건이 거짓일 때 실행할 코드를 작성합니다.

즉, if/else는 두 가지 경우 중 하나를 반드시 선택해야 할 때 사용합니다.
"""

# 점수를 하나 정해 둡니다.
# 지금은 연습을 위해 코드 안에 값을 직접 넣었습니다.
score = 85

print("점수:", score)

# score >= 80 은 "점수가 80 이상인가?"라는 조건입니다.
if score >= 80:
    # 조건이 참이면 이 코드가 실행됩니다.
    print("통과입니다.")
    print("다음 단계로 이동할 수 있습니다.")
else:
    # 조건이 거짓이면 이 코드가 실행됩니다.
    print("다시 연습이 필요합니다.")
    print("기본 개념을 복습합니다.")

print("조건문 검사가 끝났습니다.")
