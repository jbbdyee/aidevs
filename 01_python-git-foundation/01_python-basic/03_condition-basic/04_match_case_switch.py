"""match-case를 사용한 switch 방식 예제입니다.

Python에는 다른 언어의 `switch`와 완전히 같은 문법은 없지만,
Python 3.10부터 `match-case` 문법을 사용할 수 있습니다.

`match-case`는 하나의 값을 여러 경우(case)와 비교할 때 읽기 좋습니다.
메뉴 번호, 명령어, 상태값처럼 정해진 선택지가 있을 때 자주 사용합니다.
"""

# 사용자가 선택한 메뉴 번호라고 가정합니다.
# 실제 프로그램에서는 input()으로 입력받을 수도 있습니다.
menu = "2"

print("선택한 메뉴:", menu)

# match 뒤에는 비교할 기준 값을 씁니다.
match menu:
    # menu 값이 "1"이면 아래 코드가 실행됩니다.
    case "1":
        print("사용자 정보를 조회합니다.")

    # menu 값이 "2"이면 아래 코드가 실행됩니다.
    case "2":
        print("새 대화 기록을 저장합니다.")

    # menu 값이 "3"이면 아래 코드가 실행됩니다.
    case "3":
        print("서비스 로그를 확인합니다.")

    # _ 는 위 case에 모두 해당하지 않을 때 실행되는 기본 처리입니다.
    # 다른 언어의 default와 비슷하게 생각하면 됩니다.
    case _:
        print("알 수 없는 메뉴입니다.")

print("프로그램을 종료합니다.")
