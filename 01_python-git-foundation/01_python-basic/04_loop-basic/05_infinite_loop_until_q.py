"""q를 입력할 때까지 계속 실행되는 반복문 예제입니다.

이 예제는 `while True`를 사용합니다.

`while True`는 조건이 항상 참이기 때문에 그냥 두면 끝나지 않습니다.
그래서 반복문 안에서 `break`를 사용해 종료 조건을 직접 만들어야 합니다.

여기서는 사용자가 `q`를 입력하면 반복문을 종료합니다.
`q`가 아닌 값을 입력하면 프로그램은 계속 동작합니다.
"""

print("간단한 입력 반복 프로그램입니다.")
print("종료하려면 q를 입력하세요.")

# while True는 일부러 무한 반복을 만드는 문법입니다.
# 서버, 챗봇, 메뉴 프로그램처럼 사용자의 입력을 계속 기다려야 할 때 자주 사용합니다.
while True:
    # input()은 사용자가 Enter를 누를 때까지 기다립니다.
    user_input = input("메시지를 입력하세요: ")

    # strip()은 앞뒤 공백을 제거합니다.
    # lower()는 대문자를 소문자로 바꿉니다.
    # 이렇게 하면 " q ", "Q"처럼 입력해도 종료할 수 있습니다.
    command = user_input.strip().lower()

    # 사용자가 q를 입력하면 반복문을 종료합니다.
    if command == "q":
        print("q가 입력되어 프로그램을 종료합니다.")
        break

    # q가 아니면 입력한 내용을 출력하고 다시 반복문의 처음으로 돌아갑니다.
    print("입력한 값:", user_input)
    print("계속 실행 중입니다. 종료하려면 q를 입력하세요.")

print("반복문 밖으로 나왔습니다.")
