"""함수 정의와 호출 기초 예제입니다.

함수는 자주 사용하는 코드를 이름으로 묶어 두는 문법입니다.

함수를 사용하는 이유:
1. 같은 코드를 여러 번 쓰지 않아도 됩니다.
2. 코드에 이름을 붙여 의미를 분명하게 만들 수 있습니다.
3. 프로그램을 작은 단위로 나눌 수 있습니다.
"""


# def는 함수를 만들 때 사용하는 키워드입니다.
# say_hello는 함수 이름입니다.
def say_hello():
    print("안녕하세요.")
    print("Python 함수 연습을 시작합니다.")


print("함수 호출 전")

# 함수를 정의만 하면 실행되지 않습니다.
# 함수 이름 뒤에 괄호를 붙여 호출해야 실행됩니다.
say_hello()

print("함수 호출 후")

# 같은 함수를 여러 번 호출할 수 있습니다.
say_hello()
