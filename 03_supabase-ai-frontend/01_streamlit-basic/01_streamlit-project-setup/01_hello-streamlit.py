import streamlit as st  # Python 코드로 웹 화면을 만들기 위해 Streamlit을 st라는 별칭으로 가져옵니다.

st.title("Hello, Streamlit")  # Streamlit 화면의 가장 큰 제목을 표시합니다.
st.write("Python 코드로 웹 화면을 만드는 첫 번째 예제입니다.")  # 문자열, 숫자, 객체를 Streamlit 화면에 출력합니다.
st.info("왼쪽 위 메뉴에서 Rerun을 누르거나 코드를 저장하면 화면이 다시 실행됩니다.")  # 다음 행동을 안내하는 정보 메시지를 표시합니다.

