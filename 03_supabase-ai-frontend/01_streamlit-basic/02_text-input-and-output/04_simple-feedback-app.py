import streamlit as st  # Python 코드로 웹 화면을 만들기 위해 Streamlit을 st라는 별칭으로 가져옵니다.

st.title("학습 피드백 앱")  # Streamlit 화면의 가장 큰 제목을 표시합니다.

name = st.text_input("이름")  # 사용자가 입력한 이름을 문자열로 저장합니다.
score = st.number_input("오늘의 이해도 점수", min_value=0, max_value=100, value=70)  # 0부터 100 사이의 숫자를 이해도 점수로 저장합니다.
comment = st.text_area("오늘 배운 내용 한 줄 정리")  # 여러 줄 입력창에서 학습 정리 내용을 문자열로 저장합니다.

if score >= 80:  # 이해도 점수가 80점 이상이면 다음 단계로 넘어갈 수 있다는 메시지를 준비합니다.
    message = "좋습니다. 다음 실습으로 넘어갈 준비가 되어 있습니다."  # 화면에 출력할 피드백 문장을 변수에 저장합니다.
elif score >= 50:  # 80점 미만이지만 50점 이상이면 복습을 권장하는 메시지를 준비합니다.
    message = "기본 흐름은 이해했습니다. 예제를 한 번 더 수정해 보세요."  # 화면에 출력할 피드백 문장을 변수에 저장합니다.
else:  # 50점 미만이면 기초 예제를 다시 확인하라는 메시지를 준비합니다.
    message = "천천히 괜찮습니다. 첫 번째 예제부터 다시 실행해 보세요."  # 화면에 출력할 피드백 문장을 변수에 저장합니다.

if name and comment:  # 이름과 학습 정리가 모두 입력되었을 때만 결과 영역을 표시합니다.
    st.success(f"{name}님의 피드백")  # 결과 영역의 제목을 성공 메시지 형태로 표시합니다.
    st.write(message)  # 점수 조건에 따라 준비한 피드백 문장을 출력합니다.
    st.caption(f"오늘의 정리: {comment}")  # 사용자가 작성한 학습 정리를 작은 글씨로 표시합니다.
else:  # 필수 입력값이 부족하면 결과 대신 안내 메시지를 표시합니다.
    st.warning("이름과 오늘 배운 내용 한 줄 정리를 입력하세요.")  # 주의가 필요한 상황을 경고 메시지로 표시합니다.
